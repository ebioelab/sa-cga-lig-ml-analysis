def main(base_folder="."):
    """
    Execute the full ML workflow for all analytes and feature combinations.

    Parameters
    ----------
    base_folder : str, optional
        Folder containing the input pickle files.

    Returns
    -------
    tuple
        summaries : list
            Summary metrics for each analyte/mode combination.
        count_df : pandas.DataFrame
            Concentration-level count table for retained predictions.
    """
    all_records = load_all_records(base_folder=base_folder)

    # Compare single-mode and multimodal feature sets.
    mode_sets = [
        ("SWV",),
        ("DPV",),
        ("DPV", "SWV"),
    ]

    summaries = []
    count_rows = []

    for modes in mode_sets:
        mode_name = "+".join(modes)

        # Plot both analytes on the same nominal-vs-predicted figure for each mode.
        plt.figure(figsize=(7, 6))

        mode_count_rows = []

        for analyte in ["SA", "CGA"]:
            X, Y, labels = build_dataset(all_records, analyte, modes)
            threshold_label = TC_BY_ANALYTE[analyte]

            result = run_two_stage_dt(X, Y, threshold_label=threshold_label)
            summary = plot_error_pred(result, analyte, modes)
            summaries.append(summary)

            new_rows = result_to_count_rows(result, analyte, modes)
            count_rows.extend(new_rows)
            mode_count_rows.extend(new_rows)

        plt.legend(loc="center left", bbox_to_anchor=(1, 0.5))
        plt.tight_layout()
        plt.show()

        # Generate concentration-level diagnostic plots for the current mode.
        mode_count_df = pd.DataFrame(mode_count_rows)
        plot_ecdf_allowed_predictions(mode_count_df, mode_name)
        plot_count_bars(mode_count_df, mode_name)

    count_df = pd.DataFrame(count_rows)

    print("\nCOUNT TABLE PREVIEW")
    print(count_df.head(12))

    # Inspect feature importance separately for each analyte and feature set.
    for modes in mode_sets:
        for analyte in ["SA", "CGA"]:
            X, Y, labels = build_dataset(all_records, analyte, modes)
            plot_feature_importance(X, Y, analyte, modes)

    print("\nFINAL SUMMARY")
    for summary in summaries:
        print(summary)

    return summaries, count_df


